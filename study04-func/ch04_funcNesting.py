# 函数嵌套

def func1():
    print('---------------func1 start-------------------')
    print('---------------执行代码省略-------------------')
    print('---------------func1 end-------------------')
    pass


def func2():
    print('---------------func2 start-------------------')
    # 调用第一个函数
    func1()
    print('---------------func2 end-------------------')
    pass


func2()  # 调用函数2

# 函数的分类：根据函数的返回值和函数的参数
# 有参数无返回值的
# 有参数又返回值的
# 无参数又返回值的
# 无参数无返回值的
